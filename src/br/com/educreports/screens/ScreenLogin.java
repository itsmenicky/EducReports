    public static String bytesToHex(byte[] bytes) {
        StringBuilder result = new StringBuilder();
        for (byte b : bytes) {
            result.append(String.format("%02x", b));
        }
        return result.toString();
    }

    private void login() {
        String sql = "select * from tb_user where login=? and password=?";
        try {
            pst = conexao.prepareStatement(sql);
            String senha = new String(txtPass.getPassword());
            MessageDigest algorithm = MessageDigest.getInstance("MD5");
            byte[] messageDigest = algorithm.digest(senha.getBytes(StandardCharsets.UTF_8));
            String senhaCriptografada = bytesToHex(messageDigest);
            pst.setString(1, txtLogin.getText());
            pst.setString(2, senhaCriptografada);
            rs = pst.executeQuery();

            if (rs.next()) {
                MainScreen telaPrincipal = new MainScreen();
                telaPrincipal.setExtendedState(6);
                telaPrincipal.setVisible(true);
                MainScreen.menu_username.setText(rs.getString(2));
                if (rs.getString(6).equals("Admin")) {
                    MainScreen.students_menu.setEnabled(true);
                    MainScreen.users_menu.setEnabled(true);
                    MainScreen.menu_students_rel.setEnabled(true);
                    MainScreen.menu_teachers_rel.setEnabled(true);
                    MainScreen.students_menuitem.setEnabled(true);
                    MainScreen.users_menuitem.setEnabled(true);
                    Adminprofile = true;
                }
                this.dispose();
                conexao.close();
            } else {
                JOptionPane.showMessageDialog(null, "Usu√°rio/Senha incorretos");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

